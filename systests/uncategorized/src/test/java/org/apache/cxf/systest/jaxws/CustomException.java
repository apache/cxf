package org.apache.cxf.systest.jaxws;

/**
 * 
 */
public class CustomException extends Exception {
    private String a;
    private String b;

    public CustomException(String s) {
        super(s);
        a = "123";
        b = "abc";
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
